pub fn convert(degrees: f32) -> Option<&'static str> {
    match degrees {
        0.0..=11.25 => Some("N"),
        11.25..=33.75 => Some("NNE"),
        33.75..=56.25 => Some("NE"),
        56.25..=78.75 => Some("ENE"),
        78.75..=101.25 => Some("E"),
        101.25..=123.75 => Some("ESE"),
        123.75..=146.25 => Some("SE"),
        146.25..=168.75 => Some("SSE"),
        168.75..=191.25 => Some("S"),
        191.25..=213.75 => Some("SSW"),
        213.75..=236.25 => Some("SW"),
        236.25..=258.75 => Some("WSW"),
        258.75..=281.25 => Some("W"),
        281.25..=303.75 => Some("WNW"),
        303.75..=326.25 => Some("NW"),
        326.25..=348.75 => Some("NNW"),
        348.75..=360.0 => Some("N"),
        _ => None,
    }
}
